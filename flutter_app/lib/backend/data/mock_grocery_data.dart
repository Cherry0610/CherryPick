  static GroceryStoreProduct _createProduct({
    required String name,
    required String storeName,
    required double price,
    String? originalPrice,
    required String unit,
    required String category,
    String? brand,
    required String imageUrl,
    String? productUrl, // Optional: provide actual product URL
  }) {
    final storeNameLower = storeName.toLowerCase();
    String baseUrl;
    String finalProductUrl;
    
    // Use provided product URL if available, otherwise generate search URL
    if (productUrl != null && productUrl.isNotEmpty) {
      finalProductUrl = productUrl;
    } else {
      // Generate search URLs that can be used for HTML web scraping
      // The actual product URL will be fetched from the HTML when available
      if (storeNameLower.contains('jaya')) {
        baseUrl = 'https://www.jayagrocer.com';
        finalProductUrl = '$baseUrl/search?q=${Uri.encodeComponent(name)}';
      } else if (storeNameLower.contains('nsk')) {
        baseUrl = 'https://www.nskgrocer.com';
        finalProductUrl = '$baseUrl/search?q=${Uri.encodeComponent(name)}';
      } else if (storeNameLower.contains('aeon')) {
        baseUrl = 'https://www.aeon.com.my';
        finalProductUrl = '$baseUrl/en/groceries/search?q=${Uri.encodeComponent(name)}';
      } else if (storeNameLower.contains('village')) {
        baseUrl = 'https://www.villagegrocer.com';
        finalProductUrl = '$baseUrl/search?q=${Uri.encodeComponent(name)}';
      } else if (storeNameLower.contains('tesco')) {
        baseUrl = 'https://www.tesco.com.my';
        finalProductUrl = '$baseUrl/groceries/en-GB/search?query=${Uri.encodeComponent(name)}';
      } else {
        baseUrl = 'https://www.example.com';
        finalProductUrl = '$baseUrl/search?q=${Uri.encodeComponent(name)}';
      }
    }

    return GroceryStoreProduct(
      id: '${storeName.toLowerCase().replaceAll(' ', '_')}_${name.toLowerCase().replaceAll(' ', '_').hashCode}',
      name: name,
      storeName: storeName,
      price: price,
      originalPrice: originalPrice,
      currency: 'MYR',
      imageUrl: imageUrl,
      productUrl: finalProductUrl,
      brand: brand,
      category: category,
      unit: unit,
      inStock: true,
      rating: 4.0 + (price % 1.0), // Random rating between 4.0-5.0
      reviewCount: (50 + (price * 10).toInt()),
      description: 'High quality $name from $storeName. $unit package.',
    );
  }
